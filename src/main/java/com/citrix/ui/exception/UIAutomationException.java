package com.citrix.ui.exception;

/**
 * Generic UIAutomationException Exception class
 * 
 * 
 */
public class UIAutomationException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public UIAutomationException() {
		super();
	}

	public UIAutomationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public UIAutomationException(String arg0) {
		super(arg0);
	}

	public UIAutomationException(Throwable arg0) {
		super(arg0);
	}

}
